-- Миграция: таймзона клиента при бронировании.
-- Шаг C.1 для задачи 3b-2 (двусторонний жизненный цикл брони в Google).
--
-- Сейчас clientTz приходит в POST /bookings из браузера клиента и
-- используется ТОЛЬКО внутри запроса (luxon, расчёт UTC-instant), но в
-- БД не сохраняется. Это создаёт проблему для отложенных операций:
-- когда коуч позже подтверждает pending-бронь, createBookingEvent нужен
-- timezone для start.timeZone Google-события — а взять его уже неоткуда.
--
-- Колонка nullable: брони, созданные ДО этой миграции, и брони, где
-- клиент не прислал timezone (старый фронт, прямой POST из curl и т.п.),
-- останутся с NULL — это нормально. В /confirm fallback на 'UTC'.
--
-- Заполняется кодом на шаге C.2 (POST /bookings/), не миграцией.
-- Идемпотентно — миграции применяются при каждом старте.

ALTER TABLE bookings ADD COLUMN IF NOT EXISTS client_timezone TEXT;
